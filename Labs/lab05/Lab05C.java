public class Lab05C {
  public static void main(String[] args) {
    int x = 0;
    int y = 0;
    int z = 0;
    x = x + 1;     // Add one to x using normal addition.
    y += 1;     // Add one to y using arithmetic assignment.
    z ++;     // Add one to z using the increment operator.
    System.out.println("x = " + x);
    System.out.println("y = " + y);
    System.out.println("z = " + z);
  }
}

