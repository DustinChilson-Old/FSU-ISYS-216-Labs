public class Lab14B {
  public static void main(String[] args) {
    System.out.println("5.0 squared is " + squareIt(5.0));
    System.out.println("5 squared is " + squareIt(5));
  }
  public static double squareIt(double num) {
    return num * num;
  }
  public static int squareIt(int num) {
    return num * num;
  }
}
